# Import math function
import math

# Take input from user
num = float(input("Enter a number: "))

# Calculate values
square_root = math.sqrt(num)        # Square root
log_value = math.log(num)           # Natural logarithm (base e)
sine_value = math.sin(num)          # Sine (in radians)

# Display results
print("Square root:", square_root)
print("Logarithm:", log_value)
print("Sine:", sine_value)